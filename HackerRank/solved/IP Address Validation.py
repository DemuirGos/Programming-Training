import re
ipv6="^[0-9a-f]{1,4}:[0-9a-f]{1,4}:[0-9a-f]{1,4}:[0-9a-f]{1,4}:[0-9a-f]{1,4}:[0-9a-f]{1,4}:[0-9a-f]{1,4}:[0-9a-f]{1,4}$"
ipv4="^(((0?|1)[0-9]?[0-9])|(2[0-4][0-9])|(25[0-5]))\.(((0?|1)[0-9]?[0-9])|(2[0-4][0-9])|(25[0-5]))\.(((0?|1)[0-9]?[0-9])|(2[0-4][0-9])|(25[0-5]))\.(((0?|1)[0-9]?[0-9])|(2[0-4][0-9])|(25[0-5]))$"
for _ in range(int(input())):
    s=input()
    if re.match(ipv4,s):
        print("IPv4")
    elif re.match(ipv6,s):
        print('IPv6')
    else:
        print("Neither")

    
